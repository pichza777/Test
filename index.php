      <h3 class="head__1">Services</h3>
      <h4>
<h6>- รับจ้างบันทึกเสียงระดับเดโม่ เพื่อส่งค่ายเพลง ประกอบอัลบั้ม หรือส่งตามงานประกวดต่างๆ </h6><br>
ทางกลุ่มเรามีสถานที่รับบันทึกเสียง พร้อมทีมงานที่มีประสบการ์ณให้ความช่วยเหลือ และอำนวยความสะดวกตลอดการบันทึกเสียง 
และคอยให้คำแนะนำ และชี้แนะ สำหรับศิลปินมือใหม่ หรือ ตอบสนองความต้องการของศิลปินที่มีประสบการ์ณได้เป็นอย่างดี <br><br>

<h6>-รับจ้างบันทึกเสียงสดนอกสถานที่ </h6><br>
ทางทีมงาน 9'50 สามารถให้บริการลูกค้าทุกท่านในการบันทึกเสียงต่างๆของลูกค้า
ไม่ว่าจะเป็นการแสดงดนตรีสด คอนเสิร์ต รายการทีวี งานสัมนา ทุกอย่างที่ท่านต้องการให้เราบันทึก
เราสามารถบันทึกทุกเสียงที่คุณต้องการได้ทั้งในและนอกสถานที่ ด้วยอุปกรณ์ที่เพียบพร้อม
เราพร้อมบริการท่านด้วยบุคลากรที่มีประสบการณ์ในสายงานด้านนี้อย่างเชี่ยวชาญ <br><br>

<h6>- รับจ้างบันทึกเสียงตามกองถ่าย</h6><br>
ทางทีมงาน 9'50 สามารถให้บริการลูกค้าในการบันทึกเสียงในกองถ่าย (sound on location) 
และสามารถจัดทำ mixing เสียงเข้ากับทางภาพเคลื่อนไหวได้<br><br>

<h6>- รับจ้างผลิตเพลงเชิงพาณิชย์ </h6><br>
ทางกลุ่มเราสามารถรับจ้างผลิตเพลงให้กับลูกค้าทุกท่านได้ทุกรูปแบบ 
ไม่ว่าจะเป็นการผลิตเพลงประกอบ score หนังสั้น ซีรี่ยส์ โฆษณา สารคดี เกมโชว์ 
สามารถแต่งเนื้อร้อง หรือทำนองสำหรับศิลปินที่ต้องการทำเพลงประกอบอัลบั้ม 
เพลงประจำองค์กร เพลงประกอบการแข่งขัน 
ประกอบเสียงรอสาย และเพื่อการพาณิชย์ทุกประเภท 
และสามารถออกแบบธีมเพลงเฉพาะตัวละครเกมต่างๆได้ และสามารถทำ sound effect ประกอบได้เช่นกัน
 <br><br>

<h6>- รับ mix&Mastering </h6><br>
ทางเรารับทำการ mix&Mastering งานทุกประเภท
ไม่ว่าจะเป็นงานที่เกี่ยวกับทีวี วิทยุ เพลง หรืองานคอนเสิร์ต
เพื่อเสียงที่คุณต้องการใช้งานนั้น มีคุณภาพที่ดีและเสถียรมากยิ่งขึ้น
เราทำงานด้วยทีมงานที่มีประสบการณ์หลายปี   <br><br>

<h6>- รับอัดและผลิต spot วิทยุโฆษณาทุกรูปแบบ </h6><br>
เราสามารถผลิต spot วิทยุโฆษณา ตามความต้องการของรายการนั้นๆได้ 
รวมถึงผลิตงานเพลงที่ใช้ประกอบสื่อหรือรายการทางทีวี วิทยุ อินเตอร์เนททุกรูปแบบ <br><br>
<br><br>
